/**
 * Returns problems specific to the selected processes.
 * Falls back to universal problems if no match found.
 */
export function getProblemsForSelection(industryId: string, selectedProcesses: string[]): { process: string; problems: string[] }[] {
  return selectedProcesses.map(proc => ({
    process: proc,
    problems: problems[`${industryId}::${proc}`] || [
      'Ręczna praca — przepisywanie, kopiowanie, klikanie',
      'Brak jednego źródła prawdy — dane w wielu miejscach',
      'Brak widoczności — nie wiadomo co jest na jakim etapie',
      'Zależność od ludzi — jak ktoś odejdzie, proces się sypie',
      'Powtarzające się błędy i pomyłki',
    ],
  }));
}
